package com.brevy.spring.wf.biz.reservation.support;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * @Description 信用卡支付
 * @author caobin
 * @date 2013-7-1
 * @version 1.0
 */
public class CreditcardPayment {

	/**
	 * @Description 支付失败信息打印
	 * @param errMsg
	 * @author caobin
	 */
	public void printFailureMessage(String errMsg){
		log.info(">>>>>>>>>> 信用卡支付失败打印");
		System.err.println(errMsg);
	}
	
	private transient Logger log = LoggerFactory.getLogger(this.getClass());
}
